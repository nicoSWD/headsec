namespace nicoSWD\SecHeaderCheck\Domain\Result;

abstract class AbstractHeaderAuditResult
{
    private $name = '';

    abstract public function isSecure(): bool;

    public function __construct(string $name)
    {
        $this->name = $name;
    }

    public function name(): string
    {
        return $this->name;
    }
}
