namespace nicoSWD\SecHeaderCheck\Domain\Header;

final class HttpHeader
{
    /** @var string */
    private $name = '';
    /** @var string|string[] */
    private $value;

    public function __construct(string $name, $value)
    {
        $this->name = $name;
        $this->value = $value;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function getValue()
    {
        return $this->value;
    }
}
